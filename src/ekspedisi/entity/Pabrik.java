package ekspedisi.entity;

/**
 *
 * @author amateur
 */
public class Pabrik {
    private String nama;
    private String alamat;
    private String norek;
    private String narek;
    private String bank;

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the alamat
     */
    public String getAlamat() {
        return alamat;
    }

    /**
     * @param alamat the alamat to set
     */
    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    /**
     * @return the norek
     */
    public String getNorek() {
        return norek;
    }

    /**
     * @param norek the norek to set
     */
    public void setNorek(String norek) {
        this.norek = norek;
    }

    /**
     * @return the narek
     */
    public String getNarek() {
        return narek;
    }

    /**
     * @param narek the narek to set
     */
    public void setNarek(String narek) {
        this.narek = narek;
    }

    /**
     * @return the bank
     */
    public String getBank() {
        return bank;
    }

    /**
     * @param bank the bank to set
     */
    public void setBank(String bank) {
        this.bank = bank;
    }
    
    
}
